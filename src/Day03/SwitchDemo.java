package Day03;

public class SwitchDemo {

	public static void main(String[] args) {
		// TODO 自动生成的方法存根
/*
 * 		switch (表达式）
 * ｛
 * 			case 取值1：
 * 					执行语句；
 * 			case  取值2：
 * 			执行语句；
 *                break；
 *                ｝
 *                
 */
		int x=3;
		switch (x)
		{
		case 4:
			System.out.println("a");
			break;
			case 1:
			System.out.println("b");
			break;
		case 3:
			System.out.println("c");
			break;
			default:
				System.out.println("d");
				break;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
